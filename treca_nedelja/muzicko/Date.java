package muzicko;

public class Date {
	private int godina;
	private int dan;
	private int mesec;
	
	public Date(int godina, int dan, int mesec) {
		this.godina=godina;
		this.dan=dan;
		this.mesec=mesec;
	}

	public int getGodina() {
		return godina;
	}

	public void setGodina(int godina) {
		this.godina = godina;
	}
	
	

}
